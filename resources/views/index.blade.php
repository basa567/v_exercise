    function cart(id){
        let productName = document.getElementById("productname"+id).textContent;
        let price = document.getElementById("productprice"+id).textContent;
        
        $.ajax({ 
            url: "./cart",
            method: "GET",
            dataType:"json", 
            data: {
                    "id":id                                      
                }               
        }).done(function(data){      
            if(data==1){
            count++;
            var classname = "cartindividual";           
             $(".addcart").append('<div class="cartlist"><img src="img/'+productName+'.JPG" alt="bag" width="50px" height="50px">'+ productName +'1x'+price+'  &#10006;</div>');
             if(count>1){
             let showtext = count +" items in your cart";
             $(".cartDetail").text(showtext);
             }else{
                let showtext = count +" item in your cart";
                $(".cartDetail").text(showtext);
             }
             
            }
        })
        
    }

    function validateEmail(){
        let userEmail = document.getElementById("userEmail").value;
        let text = '<span class="glyphicon glyphicon-repeat" aria-hidden="true"> Subscribing to newsletter</span>';
         $(".validateResult").html(text).show();
        
        $.ajax({ 
            url: "./newsletter",
            method: "GET",
            dataType:"json", 
            data: {
                    "email":userEmail                                     
                }               
        }).done(function(data){  
            if(data==0){
                let text = '<span class="glyphicon glyphicon-remove" aria-hidden="true"> Email verification failed..</span>';
                $(".validateResult").html(text).show().css("color","red");
            }else if(data==1){
                let text = '<span class="glyphicon glyphicon-ok" aria-hidden="true"> Subscription Sucessful</span>';
                $(".validateResult").html(text).css("color","green");
            }
        })
        
    }    
